    /**
     * 验证是否为中文姓名
     * @param name 待验证的字符串
     * @return true 如果可能是中文姓名
     */
    public static boolean isValidName(String name) {
        if (name == null || name.isEmpty()) {
            return false;
        }

        // 长度检查（2-4个汉字）
        if (name.length() < 2 || name.length() > 4) {
            return false;
        }

        // 必须全是汉字
        for (char c : name.toCharArray()) {
            if (!isChinese(c)) {
                return false;
            }
        }

        // 排除检查
        if (EXCLUDE_WORDS.contains(name)) {
            return false;
        }

        // 检查是否以常见姓氏开头
        String surname = name.substring(0, 1);
        String surname2 = name.length() >= 2 ? name.substring(0, 2) : "";

        if (!SURNAMES.contains(surname) && !SURNAMES.contains(surname2)) {
            return false;
        }

        // 如果是2字姓名，直接通过
        if (name.length() == 2) {
            return true;
        }

        // 如果是3-4字姓名，检查名字部分是否包含常见用字
        String givenName = surname.length() == 1 ? name.substring(1) : name.substring(2);

        // 排除常见词汇组合
        if (EXCLUDE_WORDS.contains(givenName)) {
            return false;
        }

        for (String charStr : NAME_CHARS) {
            if (givenName.contains(charStr)) {
                return true;
            }
        }

        // 即使不包含常见用字，如果姓氏匹配，也认为可能是姓名
        return true;
    }

    /**
     * 判断是否为汉字
     */
    private static boolean isChinese(char c) {
        return c >= '一' && c <= '鿿';
    }

    /**
     * 从字符串中提取可能的中文姓名
     * @param text 待提取的字符串
     * @return 提取到的姓名列表
     */
    public static List<String> extractNames(String text) {
        List<String> names = new ArrayList<String>();
        if (text == null || text.isEmpty()) {
            return names;
        }

        // 尝试提取2-4个连续汉字
        for (int len = 4; len >= 2; len--) {
            for (int i = 0; i <= text.length() - len; i++) {
                String candidate = text.substring(i, i + len);
                if (isValidName(candidate)) {
                    names.add(candidate);
                }
            }
        }

        return names;
    }
